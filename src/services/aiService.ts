import { GoogleGenAI } from '@google/genai';
import { Resource, ColorCategory, AISettings, SentenceLength } from '../types';

// We lazily instantiate the Gemini client to avoid startup crashes if the key is completely missing
function getGeminiClient(customKey?: string) {
  const key = customKey || process.env.GEMINI_API_KEY;
  if (!key) {
    throw new Error('No Gemini API Key available. Please specify an API key in your AI Configuration.');
  }
  return new GoogleGenAI({ apiKey: key });
}

export interface GenerateChunkParams {
  resources: Resource[];
  rTotal: number;
  iValue: number;
  uTotal: number;
  theme?: string;
  sentenceLength?: SentenceLength;
  settings?: AISettings;
}

export interface GeneratedChunkResponse {
  engSentence: string;
  vieSentence: string;
  category: string;
}

export interface AutoGenerateParams {
  theme: string;
  targetU: number;
  quantity: number;
  sentenceLength: SentenceLength;
  colorPreferences: ColorCategory[];
  availableResources: Resource[];
  settings?: AISettings;
}

export interface AutoGeneratedChunk extends GeneratedChunkResponse {
  resourcesUsed: Resource[];
  rTotal: number;
  iValue: number;
  uTotal: number;
  difficultyLabel: string;
  audioUrl?: string | null;
}

export interface SemanticChunk {
  text: string;
  label: 'GREEN' | 'BLUE' | 'RED' | 'PINK' | 'NONE';
  ohm: number;
  confidence: number;
  reason: string;
}

export interface OhmAnalysisResult {
  transcriptRaw: string;
  transcriptNormalized: string;
  chunks: SemanticChunk[];
  formula: string;
  totalOhm: number;
}

export async function transcribeAudio(audioBlob: Blob, settings?: AISettings): Promise<string> {
  const base64Audio = await new Promise<string>((resolve, reject) => {
    const reader = new FileReader();
    reader.onloadend = () => {
      const base64 = (reader.result as string).split(',')[1];
      resolve(base64);
    };
    reader.onerror = reject;
    reader.readAsDataURL(audioBlob);
  });

  const apiKey = settings?.geminiApiKey || process.env.GEMINI_API_KEY;
  const aiClient = getGeminiClient(apiKey);
  const modelToUse = settings?.audioTranscriptModel || 'gemini-2.5-flash';

  const response = await aiClient.models.generateContent({
    model: modelToUse,
    contents: [
      {
        inlineData: {
          mimeType: audioBlob.type || 'audio/webm',
          data: base64Audio
        }
      },
      "Please transcribe this audio. Return ONLY the transcript text in the language spoken, with no other commentary, quotes, or formatting."
    ]
  });
  return response.text ? response.text.trim() : '';
}

export async function analyzeTranscript(transcript: string, settings?: AISettings, baseOhms?: Record<string, number>): Promise<OhmAnalysisResult> {
  const ohms = settings?.ohmBaseValues || baseOhms || { Green: 5, Blue: 7, Red: 9, Pink: 3 };
  
  const defaultInstructions = `
Bạn là một chuyên gia phân tích ngôn ngữ học tiếng Việt. Hãy bóc tách Transcript thành các cụm từ (Semantic Chunks) mang năng lượng Ohm:
- GREEN (${ohms.Green} Ohm): Quán ngữ, từ nối, từ lặp, fillers. Ví dụ: "Thành thật mà nói", "Một ngày nào đó", "ờ thì".
- BLUE (${ohms.Blue} Ohm): Khung câu, mẫu câu giao tiếp. Ví dụ: "Tôi tin rằng...", "Có thể nói là...", "Chúng ta nên...".
- RED (${ohms.Red} Ohm): Thành ngữ, từ láy, ngôn ngữ hình ảnh. Ví dụ: "Nỗi sợ", "Vượt qua", "Mạnh mẽ lên".
- PINK (${ohms.Pink} Ohm): Từ khóa chuyên môn hoặc khái niệm chính. Ví dụ: "Tâm lý học", "Kế toán".

QUY TẮC CỐT LÕI:
1. KHÔNG ĐỂ KẾT QUẢ TRỐNG: Nếu câu có nghĩa, bạn PHẢI tìm ra ít nhất một cụm từ để phân loại. Đừng quá khắt khe.
2. Ưu tiên bóc tách các cấu trúc giúp người nghe hiểu ý đồ người nói (BLUE) và các từ mang sắc thái (RED/GREEN).
3. TRÁNH VỠ VỤN: Gộp các fillers đứng cạnh nhau thành 1 chunk.
`;

  const systemInstructions = settings?.ohmPromptInstructions && settings.ohmPromptInstructions.trim() !== '' 
     ? settings.ohmPromptInstructions 
     : defaultInstructions;

  const prompt = `
${systemInstructions}

CRITICAL RULES FOR CHUNKING:
1. QUALITY OVER QUANTITY: Extract all chunks that truly fit the categories, but ignore common adjectives, basic verbs, and everyday noun phrases.
2. RED IS FOR FIGURATIVE LANGUAGE: Only extract Red if it is a true idiom, metaphor, or vivid colloquial expression. Common praise like "rất tốt", "rất đỉnh", "tuyệt vời" is NOT RED.
3. PINK IS FOR INTERMEDIATE/TECHNICAL VOCABULARY: Only extract Pink if it is a specific topic-related concept, technical term, or academic/intermediate-level vocabulary (e.g., "ứng dụng thực tế", "phát triển bền vững"). Basic nouns like "mèo", "bàn", "nước" are NOT PINK.
4. BLUE MUST BE A FRAME: It must be a reusable structure waiting for content. A complete standalone sentence is rarely BLUE.
5. NO FRAGMENTATION: Don't break phrases that should be together.
6. IGNORE FILLER: If a word adds no energy (like simple "và", "nhưng" in normal use), ignore it unless it acts as a specific discourse marker (GREEN).
7. Extract exact substrings.
8. Brief reason and confidence required.

Rules for Ohm calculation:
- GREEN = ${ohms.Green}, BLUE = ${ohms.Blue}, RED = ${ohms.Red}, PINK = ${ohms.Pink}.
- If multiple chunks have the SAME label, ADD their values.
- If chunks have DIFFERENT labels, MULTIPLY the group sums.

Transcript:
"${transcript}"

Return the result STRICTLY as a JSON object with this structure (example with 2 chunks):
{
  "transcriptRaw": "...",
  "transcriptNormalized": "...",
  "chunks": [
    {
      "text": "chunk 1",
      "label": "BLUE",
      "ohm": ${ohms.Blue},
      "confidence": 0.9,
      "reason": "..."
    },
    {
      "text": "chunk 2",
      "label": "RED",
      "ohm": ${ohms.Red},
      "confidence": 0.8,
      "reason": "..."
    }
  ],
  "formula": "string formula",
  "totalOhm": number
}
`;

  const responseText = await callAI(prompt, settings);
  try {
    const jsonMatch = responseText.match(/```json\n([\s\S]*?)\n```/) || responseText.match(/\{[\s\S]*\}/);
    const jsonString = jsonMatch ? (jsonMatch[1] || jsonMatch[0]) : responseText;
    const result = JSON.parse(jsonString) as OhmAnalysisResult;
    
    // Fallback: If AI parsed successfully but found 0 chunks for a non-empty transcript
    if ((!result.chunks || result.chunks.length === 0) && transcript.trim().length > 5) {
      result.totalOhm = 1;
      result.formula = "1 (Base Energy)";
    }
    
    return result;
  } catch (error) {
    console.error("Failed to parse analysis result:", responseText);
    throw new Error("Failed to parse analysis result from AI.");
  }
}

export async function fetchOpenRouterModels(apiKey: string, endpoint: string = 'https://openrouter.ai/api/v1') {
  if (!apiKey) return [];
  try {
    const response = await fetch(`/api/ai/models?endpoint=${encodeURIComponent(endpoint)}`, {
      method: 'GET',
      headers: {
        'Authorization': `Bearer ${apiKey}`,
      },
    });
    if (!response.ok) throw new Error(`Failed to fetch models: ${response.statusText}`);
    const data = await response.json();
    return data.data || [];
  } catch (error) {
    console.error('Error fetching models:', error);
    throw error;
  }
}

async function callAI(prompt: string, settings?: AISettings): Promise<string> {
  const apiKey = settings?.apiKey?.trim();
  const primaryModel = settings?.primaryModel || 'gemini-2.0-flash';
  const fallbackModel = settings?.fallbackModel || 'gemini-1.5-flash';

  // If user configured non-Gemini provider models but no provider key is present,
  // fail fast with a clear message instead of silently switching back to Gemini.
  const looksLikeProviderModel = (m?: string) => !!m && m.includes('/');
  if (!apiKey && (looksLikeProviderModel(primaryModel) || looksLikeProviderModel(fallbackModel))) {
    throw new Error(
      'Custom provider models are configured but API Key is empty. Please save API Endpoint + API Key in Settings > AI & LLM.'
    );
  }

  // No provider API key: use Gemini-only path.
  if (!apiKey) {
    const geminiModels = [primaryModel, fallbackModel].filter(Boolean);
    let geminiLastError: unknown = null;

    for (const model of geminiModels) {
      try {
        const gClient = getGeminiClient();
        const response = await gClient.models.generateContent({
          model,
          contents: prompt,
        });
        if (response.text) return response.text;
      } catch (error) {
        console.warn(`Gemini model ${model} failed:`, error);
        geminiLastError = error;
      }
    }

    throw geminiLastError instanceof Error
      ? geminiLastError
      : new Error('Failed to get response from Gemini');
  }

  const endpoint = settings?.endpoint;
  const modelsToTry = [primaryModel, fallbackModel].filter(Boolean);
  let lastError: unknown = null;

  for (const model of modelsToTry) {
    try {
      const response = await fetch('/api/ai/chat', {
        method: 'POST',
        headers: {
          'Content-Type': 'application/json',
          'Authorization': `Bearer ${apiKey}`,
        },
        body: JSON.stringify({
          endpoint,
          model: model,
          messages: [{ role: 'user', content: prompt }],
        }),
      });

      if (!response.ok) {
        const errorData = await response.json().catch(() => ({}));
        let errorMessage = errorData.error?.message || errorData.error || errorData.message;

        if (!errorMessage && errorData.rawResponse) {
          const raw = errorData.rawResponse;
          if (raw.includes('<html') && raw.includes('530')) {
            errorMessage = 'API Error 530: Cloudflare DNS/Origin error. The AI endpoint might be down or misconfigured.';
          } else {
            errorMessage = `API Error ${response.status}: ${raw.substring(0, 100)}...`;
          }
        }

        errorMessage = errorMessage || `API Error: ${response.status}`;
        throw new Error(typeof errorMessage === 'string' ? errorMessage : JSON.stringify(errorMessage));
      }

      const data = await response.json();

      if (data.choices && data.choices.length > 0 && data.choices[0].message) {
        return data.choices[0].message.content;
      }
      if (data.response) {
        return data.response;
      }
      if (data.content && Array.isArray(data.content)) {
        return data.content[0].text;
      }
      if (data.rawResponse) {
        return data.rawResponse;
      }

      throw new Error(`Unexpected API response format: ${JSON.stringify(data).substring(0, 200)}...`);
    } catch (error) {
      console.warn(`Failed with model ${model}:`, error);
      lastError = error;
    }
  }

  // IMPORTANT: Do not silently fall back to Gemini when provider models are configured.
  throw lastError instanceof Error
    ? lastError
    : new Error('All configured provider models failed. Check endpoint, API key, and model IDs.');
}

function cleanJSON(text: string): string {
  if (!text) return '';
  let cleanText = text.trim();
  
  // Try to extract JSON block if markdown is used
  const jsonMatch = cleanText.match(/```(?:json)?\s*([\s\S]*?)\s*```/);
  if (jsonMatch && jsonMatch[1]) {
    return jsonMatch[1].trim();
  }
  
  // Fallback: try to find the first [ or { and last ] or }
  const firstBrace = cleanText.indexOf('{');
  const lastBrace = cleanText.lastIndexOf('}');
  const firstBracket = cleanText.indexOf('[');
  const lastBracket = cleanText.lastIndexOf(']');
  
  // Determine if it's an array or object based on what comes first
  const hasBracket = firstBracket !== -1 && lastBracket !== -1;
  const hasBrace = firstBrace !== -1 && lastBrace !== -1;

  if (hasBracket && (!hasBrace || firstBracket < firstBrace)) {
    return cleanText.substring(firstBracket, lastBracket + 1);
  } else if (hasBrace) {
    return cleanText.substring(firstBrace, lastBrace + 1);
  }
  
  return cleanText;
}

export async function generateChunk(params: GenerateChunkParams): Promise<GeneratedChunkResponse> {
  const { resources, rTotal, iValue, uTotal, settings, theme, sentenceLength } = params;
  const resourceList = resources.map(r => `${r.name} (${r.color}, ${r.ohm} Ohm)`).join(', ');
  
  const currentLen = sentenceLength || 'Medium';
  const constraints = settings?.sentenceConstraints?.[currentLen];
  
  const maxSentences = constraints?.maxSentences || (currentLen === 'Very Short' ? 1 : currentLen === 'Short' ? 2 : currentLen === 'Medium' ? 3 : 5);
  const maxWords = constraints?.maxWords || (currentLen === 'Very Short' ? 15 : currentLen === 'Short' ? 30 : currentLen === 'Medium' ? 60 : 100);

const prompt = `
You are an expert linguist and curriculum designer for an EdTech system called "CHUNKS".
Your task is to generate a bilingual sentence (Vietnamese first, then English) based on a set of input resources and a specific algorithm.

Target Theme/Topic: ${theme || 'General Conversation'}
Desired Sentence Length: ${currentLen}

Algorithm Context:
- U = I * R
- R (Resistance): Base difficulty of resources. Total R = ${rTotal}
- I (Current/MSE): Context/complexity variable. I = ${iValue}
- U (Voltage): Total lesson energy/difficulty. U = ${uTotal}

Input Resources to include in the sentence:
${resourceList}

Instructions:
1. Primary Goal (Vietnamese First): Start by coming up with a highly natural, meaningful, and contextually logical Vietnamese sentence (or paragraph) that accurately incorporates the exact meaning of all the input resources. Do NOT just list them. Think deeply to create a coherent scenario.
2. English Translation: Translate that Vietnamese concept into a natural-sounding English equivalent. The English version must carry the same semantic integrity and clear intent.
3. Length Calibration: Strictly follow the length constraints for "${currentLen}": 
   - You MUST generate MAXIMUM ${maxSentences} sentence(s).
   - Total word count MUST be under ${maxWords} words.
   - If "Very Short", ensure exactly 1 small sentence.
4. Theme Adherence: The context MUST strictly revolve around the theme: ${theme || 'General'}.
5. Energy Level: The structural complexity and vocabulary choice should reflect the U (Voltage) value: ${uTotal}. Higher U means more sophisticated grammar and nuanced meaning.
6. Classification: Assign a relevant thematic category.
7. Output Format: Return ONLY JSON. Ensure the final output is meaningful and accurately uses the input parameters.

Output MUST be strictly in the following JSON format:
{
  "engSentence": "...",
  "vieSentence": "...",
  "category": "...",
  "evaluation": "Explanation of how the resources create a meaningful context matching the U: ${uTotal}"
}
`;
  const responseText = await callAI(prompt, settings);
  const cleanText = cleanJSON(responseText);
  try {
    return JSON.parse(cleanText) as GeneratedChunkResponse & { evaluation?: string };
  } catch (e) {
    console.error("Failed to parse AI response:", cleanText);
    throw new Error(`AI returned invalid JSON format: ${e instanceof Error ? e.message : 'Unknown error'}`);
  }
}

export async function generateAutoChunks(params: AutoGenerateParams): Promise<AutoGeneratedChunk[]> {
  const { theme, targetU, quantity, sentenceLength, colorPreferences, availableResources, settings } = params;

  const currentLen = sentenceLength || 'Medium';
  const constraints = settings?.sentenceConstraints?.[currentLen];
  const maxSentences = constraints?.maxSentences || (currentLen === 'Very Short' ? 1 : currentLen === 'Short' ? 2 : currentLen === 'Medium' ? 3 : 5);
  const maxWords = constraints?.maxWords || (currentLen === 'Very Short' ? 15 : currentLen === 'Short' ? 30 : currentLen === 'Medium' ? 60 : 100);

  // Pre-filter resources to balance them based on color preferences and target U
  const filteredAvailable = availableResources
    .filter(r => colorPreferences.length === 0 || colorPreferences.includes(r.color))
    .sort((a, b) => Math.abs(a.ohm - (targetU / 3)) - Math.abs(b.ohm - (targetU / 3))) // Heuristic: try to find resources that are roughly 1/3 of target U
    .slice(0, 150);

  const resourceList = filteredAvailable.map(r => ({
    id: r.id,
    name: r.name,
    color: r.color,
    ohm: r.ohm
  }));

  const formulaType = settings?.formulaType || 'sum';
  const physicsLogic = formulaType === 'sum' 
    ? `- R_total = Sum of all individual resource Ohms (R1 + R2 + R3 + ...). (Example: 5 + 9 + 3 = 17)`
    : `- R_total = Sum(R) for same-color series, then Product of those sums for different colors.`;

const prompt = `
You are a Master Linguistic Architect for the "CHUNKS" EdTech system.
Your mission is to construct ${quantity} high-quality learning chunks.

Theme: ${theme}
Target Voltage (U): ${targetU}
Sentence Length Constraints ("${currentLen}"):
- MAXIMUM ${maxSentences} sentence(s)
- Total word count MUST be under ${maxWords} words.
- If "Very Short", ensure exactly 1 short sentence.
Color Preferences: ${colorPreferences.join(', ')}

Available Ingredients (Resources):
${JSON.stringify(resourceList)}

Physics Logic:
${physicsLogic}
- U = Complexity Multiplier * R_total.

Construction Flow:
1. Resource Selection: Pick 2-4 resources that mathematically approach the Target U.
2. Vietnamese First Draft: Think deeply and construct a logical, highly natural Vietnamese sentence (or paragraph, reflecting the Sentence Length constraint) encompassing the resources.
3. English Translation: Translate that Vietnamese concept into natural English.
4. Evaluation: SELF-CRITIQUE the sentence. Does it make sense? Is the length roughly around ${maxSentences} sentences and under ${maxWords} words? If not, REGENERATE.
5. Final Validation: Ensure all resources used are from the provided list.

Output strictly as JSON array:
[
  {
    "engSentence": "...",
    "vieSentence": "...",
    "category": "...",
    "resourcesUsed": [...],
    "rTotal": 12.5,
    "iValue": 1.2,
    "uTotal": 15.0,
    "difficultyLabel": "...",
    "evaluation": "Passed: Natural and meaningful."
  }
]
`;

  const responseText = await callAI(prompt, settings);
  const cleanText = cleanJSON(responseText);
  try {
    let results = JSON.parse(cleanText);
    
    // If AI returned a single object instead of an array, wrap it
    if (!Array.isArray(results)) {
      if (typeof results === 'object' && results !== null) {
        // Sometimes AI wraps the array in an object like { "chunks": [...] }
        const possibleArray = Object.values(results).find(v => Array.isArray(v));
        if (possibleArray) {
          results = possibleArray;
        } else {
          results = [results];
        }
      } else {
        throw new Error("Parsed JSON is neither an object nor an array.");
      }
    }
    
    // Filter out any that failed internal AI evaluation if the AI was honest
    return (results as (AutoGeneratedChunk & { evaluation?: string })[])
      .filter(r => !r.evaluation || r.evaluation.toLowerCase().includes('pass'));
  } catch (e) {
    console.error("Failed to parse AI response:", cleanText);
    throw new Error(`AI returned invalid JSON format: ${e instanceof Error ? e.message : 'Unknown error'}`);
  }
}
