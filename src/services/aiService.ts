export async function generateAutoChunks(params: AutoGenerateParams): Promise<AutoGeneratedChunk[]> {
  const response = await fetch('/api/chunks/ai-batch-generate', {
    method: 'POST',
    headers: {
      'Content-Type': 'application/json',
    },
    body: JSON.stringify({
      resources: params.availableResources,
      targetR: params.targetU,
      quantity: params.quantity,
      sentenceLength: params.sentenceLength,
      preferredColors: params.colorPreferences,
      theme: params.theme,
      settings: params.settings,
      reviewEnabled: true,
      maxAttemptsPerChunk: 3,
    }),
  });

  const data = await response.json().catch(() => ({}));
  if (!response.ok) {
    throw new Error(data?.error || 'Failed to generate smart AI chunks');
  }

  return (data?.chunks || []) as AutoGeneratedChunk[];
}
