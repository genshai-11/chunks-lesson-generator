import { Resource, ColorCategory, AISettings } from '../types';

export interface GenerateChunkParams {
  resources: Resource[];
  iValue: number;
  sentenceLength?: 'Short' | 'Medium' | 'Long';
  theme?: string;
  settings?: AISettings;
  reviewEnabled?: boolean;
  generateAudio?: boolean;
}

export interface GeneratedChunkResponse {
  engSentence: string;
  vieSentence: string;
  category: string;
}

export interface ManualGeneratedChunk extends GeneratedChunkResponse {
  resourcesUsed: Resource[];
  rTotal: number;
  iValue: number;
  uTotal: number;
  difficultyLabel: string;
  audioUrl?: string | null;
  qualityScore?: number;
  reviewIssues?: string[];
  generationAttempt?: number;
}

export interface ManualBatchGenerateParams {
  resources: Resource[];
  iValue: number;
  quantity: number;
  sentenceLength?: 'Short' | 'Medium' | 'Long';
  variationMode?: 'paraphrase' | 'scenario' | 'dialogue' | 'mixed';
  theme?: string;
  settings?: AISettings;
  reviewEnabled?: boolean;
  maxAttemptsPerChunk?: number;
  generateAudio?: boolean;
}

export interface ManualBatchGenerateResponse {
  totalRequested: number;
  totalSucceeded: number;
  totalFailed: number;
  chunks: ManualGeneratedChunk[];
  failures: Array<{
    index: number;
    reason: string;
  }>;
}

export interface AutoGenerateParams {
  theme: string;
  targetU: number;
  quantity: number;
  sentenceLength: 'Short' | 'Medium' | 'Long';
  colorPreferences: ColorCategory[];
  availableResources: Resource[];
  settings?: AISettings;
}

export interface AutoGeneratedChunk extends GeneratedChunkResponse {
  resourcesUsed: Resource[];
  rTotal: number;
  iValue: number;
  uTotal: number;
  difficultyLabel: string;
  audioUrl?: string | null;
}

export interface ResourceComboSearchParams {
  resources: Resource[];
  targetU: number;
  iValue?: number;
  tolerance?: number;
  quantity?: number;
  minResources?: number;
  maxResources?: number;
  preferredColors?: ColorCategory[];
}

export async function fetchOpenRouterModels(apiKey: string, endpoint: string = 'https://openrouter.ai/api/v1') {
  if (!apiKey) return [];
  try {
    const response = await fetch(`/api/ai/models?endpoint=${encodeURIComponent(endpoint)}`, {
      method: 'GET',
      headers: {
        'Authorization': `Bearer ${apiKey}`,
      },
    });
    if (!response.ok) throw new Error(`Failed to fetch models: ${response.statusText}`);
    const data = await response.json();
    return data.data || [];
  } catch (error) {
    console.error('Error fetching models:', error);
    throw error;
  }
}

export async function generateChunk(params: GenerateChunkParams): Promise<ManualGeneratedChunk> {
  const response = await fetch('/api/chunks/generate', {
    method: 'POST',
    headers: {
      'Content-Type': 'application/json',
    },
    body: JSON.stringify({
      resources: params.resources,
      iValue: params.iValue,
      sentenceLength: params.sentenceLength || 'Medium',
      theme: params.theme,
      settings: params.settings,
      reviewEnabled: params.reviewEnabled ?? true,
      generateAudio: params.generateAudio ?? true,
    }),
  });

  const data = await response.json().catch(() => ({}));
  if (!response.ok || !data?.success || !data?.chunk) {
    throw new Error(data?.failureReason || data?.error || 'Failed to generate chunk');
  }

  return data.chunk as ManualGeneratedChunk;
}

