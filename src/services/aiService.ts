export async function fetchOpenRouterModels(apiKey: string, endpoint: string = 'https://openrouter.ai/api/v1') {
  if (!apiKey) return [];
  try {
    const response = await fetch(`/api/ai/models?endpoint=${encodeURIComponent(endpoint)}`, {
      method: 'GET',
      headers: {
        'Authorization': `Bearer ${apiKey}`,
      },
    });
    if (!response.ok) throw new Error(`Failed to fetch models: ${response.statusText}`);
    const data = await response.json();
    return data.data || [];
  } catch (error) {
    console.error('Error fetching models:', error);
    throw error;
  }
}

async function callAI(prompt: string, settings?: AISettings): Promise<string> {
  if (!settings || !settings.apiKey) {
    // Use default Gemini
    const response = await defaultAi.models.generateContent({
      model: 'gemini-2.0-flash',
      contents: prompt,
    });
    if (!response.text) throw new Error('No response from Gemini');
    return response.text;
  }

  const { endpoint, apiKey, primaryModel, fallbackModel } = settings;
  const modelsToTry = [primaryModel, fallbackModel].filter(Boolean);
  let lastError = null;

  for (const model of modelsToTry) {
    try {
      const response = await fetch('/api/ai/chat', {
        method: 'POST',
        headers: {
          'Content-Type': 'application/json',
          'Authorization': `Bearer ${apiKey}`,
        },
        body: JSON.stringify({
          endpoint,
          model: model,
          messages: [{ role: 'user', content: prompt }],
        }),
      });

      if (!response.ok) {
        const errorData = await response.json().catch(() => ({}));
        const errorMessage = errorData.error?.message || errorData.error || errorData.message || `API Error: ${response.status}`;
        throw new Error(typeof errorMessage === 'string' ? errorMessage : JSON.stringify(errorMessage));
      }

      const data = await response.json();
      return data.choices[0].message.content;
    } catch (error) {
      console.warn(`Failed with model ${model}:`, error);
      lastError = error;
    }
  }

  throw lastError || new Error('All models failed');
}

function cleanJSON(text: string): string {
  let cleanText = text.trim();
  if (cleanText.startsWith('```json')) {
    cleanText = cleanText.replace(/^```json\n?/, '').replace(/\n?```$/, '');
  } else if (cleanText.startsWith('```')) {
    cleanText = cleanText.replace(/^```\n?/, '').replace(/\n?```$/, '');
  }
  return cleanText;
}

export async function generateChunk(params: GenerateChunkParams): Promise<ManualGeneratedChunk> {
  const response = await fetch('/api/chunks/generate', {
    method: 'POST',
    headers: {
      'Content-Type': 'application/json',
    },
    body: JSON.stringify({
      resources: params.resources,
      iValue: params.iValue,
      sentenceLength: params.sentenceLength || 'Medium',
      theme: params.theme,
      settings: params.settings,
      reviewEnabled: params.reviewEnabled ?? true,
      generateAudio: params.generateAudio ?? true,
    }),
  });

  const data = await response.json().catch(() => ({}));
  if (!response.ok || !data?.success || !data?.chunk) {
    throw new Error(data?.failureReason || data?.error || 'Failed to generate chunk');
  }

  return data.chunk as ManualGeneratedChunk;
}

