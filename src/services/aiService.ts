import { GoogleGenAI } from '@google/genai';
import { Resource, ColorCategory, AISettings } from '../types';

// Default Gemini client (fallback if no custom settings)
const defaultAi = new GoogleGenAI({ apiKey: process.env.GEMINI_API_KEY });

export interface GenerateChunkParams {
  resources: Resource[];
  rTotal: number;
  iValue: number;
  uTotal: number;
  settings?: AISettings;
}

export interface GeneratedChunkResponse {
  engSentence: string;
  vieSentence: string;
  category: string;
}

export interface AutoGenerateParams {
  theme: string;
  targetU: number;
  quantity: number;
  sentenceLength: 'Short' | 'Medium' | 'Long';
  colorPreferences: ColorCategory[];
  availableResources: Resource[];
  settings?: AISettings;
}

export interface AutoGeneratedChunk extends GeneratedChunkResponse {
  resourcesUsed: Resource[];
  rTotal: number;
  iValue: number;
  uTotal: number;
  difficultyLabel: string;
}

export async function fetchOpenRouterModels(apiKey: string, endpoint: string = 'https://openrouter.ai/api/v1') {
  if (!apiKey) return [];
  try {
    const response = await fetch(`/api/ai/models?endpoint=${encodeURIComponent(endpoint)}`, {
      method: 'GET',
      headers: {
        'Authorization': `Bearer ${apiKey}`,
      },
    });
    if (!response.ok) throw new Error(`Failed to fetch models: ${response.statusText}`);
    const data = await response.json();
    return data.data || [];
  } catch (error) {
    console.error('Error fetching models:', error);
    throw error;
  }
}

async function callAI(prompt: string, settings?: AISettings): Promise<string> {
  if (!settings || !settings.apiKey) {
    // Use default Gemini
    const response = await defaultAi.models.generateContent({
      model: 'gemini-2.0-flash',
      contents: prompt,
    });
    if (!response.text) throw new Error("No response from Gemini");
    return response.text;
  }

  const { endpoint, apiKey, primaryModel, fallbackModel } = settings;
  const modelsToTry = [primaryModel, fallbackModel].filter(Boolean);
  let lastError = null;

  for (const model of modelsToTry) {
    try {
      const response = await fetch('/api/ai/chat', {
        method: 'POST',
        headers: {
          'Content-Type': 'application/json',
          'Authorization': `Bearer ${apiKey}`,
        },
        body: JSON.stringify({
          endpoint,
          model: model,
          messages: [{ role: 'user', content: prompt }],
        }),
      });

      if (!response.ok) {
        const errorData = await response.json().catch(() => ({}));
        throw new Error(errorData.error?.message || `API Error: ${response.status}`);
      }

      const data = await response.json();
      return data.choices[0].message.content;
    } catch (error) {
      console.warn(`Failed with model ${model}:`, error);
      lastError = error;
    }
  }

  throw lastError || new Error('All models failed');
}

function cleanJSON(text: string): string {
  let cleanText = text.trim();
  if (cleanText.startsWith('```json')) {
    cleanText = cleanText.replace(/^```json\n?/, '').replace(/\n?```$/, '');
  } else if (cleanText.startsWith('```')) {
    cleanText = cleanText.replace(/^```\n?/, '').replace(/\n?```$/, '');
  }
  return cleanText;
}

export async function generateChunk(params: GenerateChunkParams): Promise<GeneratedChunkResponse> {
  const { resources, rTotal, iValue, uTotal, settings } = params;
  const resourceList = resources.map(r => `${r.name} (${r.color}, ${r.ohm} Ohm)`).join(', ');

  const prompt = `
You are an expert linguist and curriculum designer for an EdTech system called "CHUNKS".
Your task is to generate a bilingual sentence (English and Vietnamese) based on a set of input resources and a specific algorithm.

Algorithm Context:
- U = I * R
- R (Resistance): Base difficulty of resources. Total R = ${rTotal}
- I (Current/MSE): Context/complexity variable. I = ${iValue}
- U (Voltage): Total lesson energy/difficulty. U = ${uTotal}

Input Resources to include in the sentence:
${resourceList}

Instructions:
1. Create a natural-sounding English sentence that meaningfully incorporates ALL the provided input resources.
2. The sentence complexity should reflect the U (Voltage) value: ${uTotal}.
3. Provide an accurate and natural Vietnamese translation.
4. Assign a thematic category (e.g., "Daily Life", "Business").

Output MUST be strictly in the following JSON format:
{
  "engSentence": "...",
  "vieSentence": "...",
  "category": "..."
}
`;

  const responseText = await callAI(prompt, settings);
  const cleanText = cleanJSON(responseText);
  return JSON.parse(cleanText) as GeneratedChunkResponse;
}

export async function generateAutoChunks(params: AutoGenerateParams): Promise<AutoGeneratedChunk[]> {
  const { theme, targetU, quantity, sentenceLength, colorPreferences, availableResources, settings } = params;

  const filteredAvailable = availableResources
    .filter(r => colorPreferences.length === 0 || colorPreferences.includes(r.color))
    .slice(0, 100);

  const resourceList = filteredAvailable.map(r => ({
    id: r.id,
    name: r.name,
    color: r.color,
    ohm: r.ohm
  }));

  const prompt = `
You are an expert linguist and curriculum designer for an EdTech system called "CHUNKS".
Your task is to automatically generate ${quantity} learning chunks based on a specific theme and difficulty target.

Theme: ${theme}
Target Voltage (U): ${targetU}
Sentence Length: ${sentenceLength}
Color Preferences: ${colorPreferences.join(', ')}

Available Resources:
${JSON.stringify(resourceList)}

Algorithm Context:
- R_total = Sum(R) if same color, else Product(R_color_totals)
- U = I * R_total

Instructions:
1. Pick 2-5 resources from the list.
2. Use EXACT text.
3. Length: ${sentenceLength}.
4. Output strictly as JSON array of objects.

Format:
[
  {
    "engSentence": "...",
    "vieSentence": "...",
    "category": "...",
    "resourcesUsed": [{"id": "...", "name": "...", "color": "...", "ohm": 3}],
    "rTotal": 12.5,
    "iValue": 1.2,
    "uTotal": 15.0,
    "difficultyLabel": "Beginner"
  }
]
`;

  const responseText = await callAI(prompt, settings);
  const cleanText = cleanJSON(responseText);
  return JSON.parse(cleanText) as AutoGeneratedChunk[];
}
